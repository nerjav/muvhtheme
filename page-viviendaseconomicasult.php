                                        <div class="cintillos"><a style="color: #fff">Viviendas Económicas</a></div>
                    
                                         <p>Viviendas Económicas<source srcset="https://www.muvh.gov.py/sitio/wp-content/uploads/2023/04/unnamed-2.jpg">El programa Viviendas Económicas tiene como objetivo reducir el déficit habitacional de las&nbsp; familias de nivel de ingresos medio otorgando facilidad en el acceso a la casa propia a través de tasas de interés por debajo de las del mercado, a plazos cómodos y cuotas fijas y accesibles.</p>
                                         <div class="cintillos"><a style="color: #fff">A qué público está dirigido</a></div>
                    
                                        
                    <p>El programa está dirigido a núcleos familiares de ingresos entre 1,5 y 5 Unidades de Salarios Mínimos (ingresos familiares) y se compongan de una de las siguientes formas: madre/padre soltera/o, divorciada/o, viuda/o, padres casados o concubinos con hijos o sin hijos, hijos sostén.</p>
                    <div class="cintillos"><a style="color: #fff">En el caso de Grupos Familiares</a></div>
                    
                    
                    <p>Los postulantes tienen la posibilidad de estar constituidos de la siguiente manera:</p>
                    <p>Su cónyuge o concubino/a; los hijos menores de edad no emancipados del/la postulante, de su cónyuge o concubino/a que habiten con ellos, incluidos aquellos que cumplan con la mayoría de edad durante el año calendario en que se postula.<br>
                    Los hijos del/la postulante, de su cónyuge o concubino/a, con algún tipo de discapacidad sean menores o mayores de edad y que habiten con ellos.<br>
                    Las personas de las cuales el/la postulante, su cónyuge o concubino/a, tenga a su cargo la tutela o curatela legal, o la obligación de prestar alimentos de acuerdo a la legislación civil y que residan con ellos.<br>
                    Padre y/o madre que conforman un único grupo familiar – Hijo/a mayor de edad que conforman un único grupo familiar.</p>
                    <div class="cintillos"><a style="color: #fff">Cómo acceder</a></div>
                    
                    
                    <p>Una vez que los proyectos se encuentren en etapa final de construcción de obra, o en proceso de Recepción Final de la misma, se realiza una Convocatoria Pública de Postulación con un plazo de recepción de documentos establecidos. Dentro de este plazo, se recibirán las postulaciones en el Departamento de Mesa de Entrada de la Sede Central de la Institución y/o de las Agencias Regionales correspondientes.</p>
                    <p>Las solicitudes ingresadas son sometidas a una evaluación compuesta de dos etapas: 1) la evaluación económica y 2) la evaluación social.</p>
                    <p>Primeramente, se procede a una evaluación económica a fin de verificar que las postulaciones reúnan toda la documentación requerida y si los postulantes son sujetos de crédito de acuerdo a los parámetros establecidos por el MUVH. A continuación, las solicitudes que reúnan las condiciones económicas pasan a la siguiente etapa, la evaluación social. Ambas evaluaciones, económica y social, se realizan implementando una Matriz de Evaluación Económica y una Matriz de Evaluación Social, que devuelven un resultado numérico que califica la situación socioeconómica de cada familia.</p>
                    <p>Resultarán seleccionadas aquellas solicitudes que obtengan mayor puntaje final en orden decreciente hasta completarse los cupos según la cantidad de viviendas disponibles. En caso de empate en la calificación total, se tendrá en cuenta el que obtenga mayor calificación social. En caso de persistir el empate se dará prioridad al puntaje más alto obtenido en la Matriz de Evaluación Social.</p>
                    <p>Al culminarse las evaluaciones se realiza la publicación de un listado de beneficiarios seleccionados y no seleccionados, con sus puntajes correspondientes de manera descendente y con las observaciones correspondientes a cada expediente.</p>
                    <p>La distribución de las viviendas disponibles a los beneficiarios seleccionados se realizará a través de un sorteo de acuerdo al plano de loteamiento del inmueble, contando, de ser posible, con la presencia de los beneficiarios seleccionados.</p>
